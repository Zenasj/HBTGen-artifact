def GetInput():
    # Generate random integer sequences with batch size 64 and sequence length 40 (max length)
    BATCH_SIZE = 64
    SEQ_LEN = 40
    input_vocab_size = 8194

    # Inputs are token IDs between 0 and vocab_size - 1
    inp = tf.random.uniform(shape=(BATCH_SIZE, SEQ_LEN), minval=1,
                            maxval=input_vocab_size-2, dtype=tf.int64)
    tar = tf.random.uniform(shape=(BATCH_SIZE, SEQ_LEN), minval=1,
                            maxval=input_vocab_size-2, dtype=tf.int64)

    return (inp, tar)

